
import java.util.Scanner;

public class Incrementacao {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        for (int num = 1; num <= 51; num++) {
          
                System.out.println("Incrementaçao de 1 em 1 = " + num);
            } 

            System.out.println("\n");

        for(int num = 52; num <= 100; num+=2) {
            System.out.println("Incrementaçao de 2 em 2 = " + num);

        }
        sc.close();
        }
        
    }

